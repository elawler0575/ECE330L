/* USER CODE BEGIN 4 */
void Seven_Segment_Digit (unsigned char digit, unsigned char hex_char)
{
	/*******************************************************************************
	Code to mask and bit shift 0-7 value of digit and 0-15 value of hex_char
	to output correct bit pattern to GPIO_Output
	*******************************************************************************/
	// 7-segment hex display patterns (active-high)
	static const unsigned char seg_pattern[16] = {

		0xc0, // 0
		0xf9, // 1
		0xa4, // 2
		0xb0, // 3
		0x99, // 4
		0x92, // 5
		0x82, // 6
		0xf8, // 7
		0x80, // 8
		0x90, // 9
		0x88, // A
		0x83, // b
		0xc6, // C
		0xa1, // d
		0x86, // E
		0x8e  // F
	};


	unsigned int pattern = seg_pattern[hex_char & 0x0f];

	// Set selected digit to 0, all others high, and output 7 segment pattern
	// Mask out invalid inputs
	    digit &= 0x07;
	    hex_char &= 0x0F;

	    //put pattern to port
	    GPIOE->ODR = pattern;

	    // Activate the desired digit on Port E bits [15:8].

	    uint16_t select_mask = 1 << (digit + 8);
	    GPIOE->ODR = select_mask;

	    // delay
	    HAL_Delay(1);

		return;
}



void Seven_Segment(unsigned int HexValue)
{
/******************************************************************************
Use a for loop to output HexValue to 7 segment display digits
*******************************************************************************/
	char digit;
	// Send hex values to lower 4 digits
	for (digit=0 ; digit<8 ; digit++)
	{
		// call Seven segment digit function with the correct hex characters
		// using bit shifting and masking
	   Seven_Segment_Digit(digit,(HexValue >> (4 * digit)) & 0x0f);
	}

	return;
}
